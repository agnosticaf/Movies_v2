//hashing a password before saving it to the database
memberSchema.pre('save', function (next) {
  var user = this;
  bcrypt.hash(user.password, 10, function (err, hash) {
    if (err) {
      return next(err);
    }
    user.password = hash;
    next();
  })
});


module.exports = mongoose.model('Member', memberSchema);
