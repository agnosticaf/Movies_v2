var mongoose = require('mongoose');
var bcrypt = require('bcrypt');
var Schema = mongoose.Schema;

var memberSchema = new mongoose.Schema({
  username: {
    type: String,
    unique: true,
    required: true,
    trim: true
  },
  password: {
    type: String,
    required: true,
  },
    updated_at: { type: Date, default: Date.now },
    videos: [{ type: Schema.Types.ObjectId, ref: 'Video' }],
});

